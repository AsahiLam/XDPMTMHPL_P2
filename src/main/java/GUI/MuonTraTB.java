    private void btnTraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTraActionPerformed
        // TODO add your handling code here:
        ttsdBLL.traThietBi(maTTTra, date);
        int choice = JOptionPane.showConfirmDialog(null, "Xác nhận trả?", "Trả thiết bị", JOptionPane.YES_NO_OPTION);
        if (choice == JOptionPane.YES_OPTION) {
            if (maTTTra != -1) {
                ttsdBLL.traThietBi(maTTTra, date);
                listThietBi();
                tbTraThietBi();
                tbMuonThietBi();
                reset();
            } else {
                JOptionPane.showMessageDialog(null, "Vui lòng chọn phiếu thông tin cần trả");
            }
        } else {
            return;
        }
    }//GEN-LAST:event_btnTraActionPerformed

