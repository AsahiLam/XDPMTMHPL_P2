    private void listThietBiValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listThietBiValueChanged
        // TODO add your handling code here:
        if (!evt.getValueIsAdjusting()) {
            String selectedThietBi = listThietBi.getSelectedValue();
            if (selectedThietBi != null) {
                String[] part = selectedThietBi.split(" - ");
                int maTB = Integer.parseInt(part[0]);
                ThietBiBLL tbBLL = new ThietBiBLL();
                thietBiMuon = tbBLL.getThietBi(maTB);
            }
        }
    }//GEN-LAST:event_listThietBiValueChanged

