    private void tbTraTBMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbTraTBMouseClicked
        // TODO add your handling code here:
        int selectedRow = tbTraTB.getSelectedRow();

        if (selectedRow != -1) {
            String str = String.valueOf(tbTraTB.getValueAt(selectedRow, 0));
            maTTTra = Integer.parseInt(str);
            ThongTinSuDung ttsd = ttsdBLL.getThongTinSuDung(maTTTra);
            if (ttsd != null) {
                txtMaTT2.setText(String.valueOf(ttsd.getMaTT()));
                txtMaTV2.setText(String.valueOf(ttsd.getThanhVien().getMaTV()));
                txtHoten2.setText(String.valueOf(ttsd.getThanhVien().getHoTen()));
                txtMaTB2.setText(String.valueOf(ttsd.getThietBi().getMaTB()));
                txtTenTB2.setText(String.valueOf(ttsd.getThietBi().getTenTB()));
                txtMoTaTB2.setText(String.valueOf(ttsd.getThietBi().getMoTaTB()));
                txtTGMuon2.setText(String.valueOf(ttsd.getTgMuon()));
            }
        }
    }//GEN-LAST:event_tbTraTBMouseClicked

    private void txtMaTVKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtMaTVKeyReleased
        // TODO add your handling code here:
        ThanhVienBLL tvBLL = new ThanhVienBLL();
        String input = txtMaTV.getText();
        if (!input.isEmpty() && input.matches("\\d+")) {
            try {
                Thread.sleep(500);
            } catch (InterruptedException e) {
                e.printStackTrace();
            }
            tvMuon = tvBLL.getThanhVien(input);
            if (tvMuon != null) {
                txtHoTen.setText(tvMuon.getHoTen());
                txtKhoa.setText(tvMuon.getKhoa());
                txtNganh.setText(tvMuon.getNganh());
                txtSDT.setText(tvMuon.getSDT());
                txtEmail.setText(tvMuon.getEmail());
            }
        }
    }//GEN-LAST:event_txtMaTVKeyReleased

