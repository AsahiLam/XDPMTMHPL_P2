    public void listThietBi() {
        ThietBiBLL tbBLL = new ThietBiBLL();
        DefaultListModel<String> model = new DefaultListModel<>(); // Tạo DefaultListModel mới

        for (ThietBi tb : tbBLL.getListThietBi()) {
            boolean check = true;
            for (ThongTinSuDung ttsd : ttsdBLL.getListThongTinSuDung()) {
                if (ttsd.getThietBi() != null && ttsd.getThietBi().equals(tb) && ttsd.getTgTra() == null) {
                    check = false;
                    break;
                }
            }
            if (check) {
                model.addElement(tb.getMaTB() + " - " + tb.getTenTB());
            }
        }

        listThietBi.setModel(model);
    }

    public void tbMuonThietBi() {
        DefaultTableModel model = (DefaultTableModel) tbMuonTB.getModel();
        model.setRowCount(0);
        for (ThongTinSuDung ttsd : ttsdBLL.getListThongTinSuDung()) {
            if (ttsd.getTgTra() != null) {
                Object[] object = {ttsd.getMaTT(), ttsd.getThanhVien().getMaTV(), ttsd.getThietBi().getMaTB(), ttsd.getTgMuon(), ttsd.getTgTra()};
                model.addRow(object);
            }
        }
        txtTGMuon.setText(String.valueOf(date));
    }

    public void tbTraThietBi() {
        DefaultTableModel model = (DefaultTableModel) tbTraTB.getModel();
        model.setRowCount(0);
        for (ThongTinSuDung ttsd : ttsdBLL.getListThongTinSuDung()) {
            if (ttsd.getTgMuon() != null && ttsd.getTgTra() == null) {
                Object[] object = {ttsd.getMaTT(), ttsd.getThanhVien().getMaTV(), ttsd.getThietBi().getMaTB(), ttsd.getTgMuon(), ttsd.getTgTra()};
                model.addRow(object);
            }
        }
    }

